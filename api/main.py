"""
api/main.py
-----------
FastAPI application that exposes the trained churn model as a REST service.

Endpoints
─────────
  GET  /health          Liveness probe — used by Docker / K8s and load balancers.
  GET  /model-info      Readiness probe — confirms model artefacts are loaded.
  POST /predict         Predict churn for a single customer.
  POST /predict-batch   Score a list of customers in one request.

Design notes
────────────
• Artefacts (model + preprocessor) are loaded ONCE during application startup
  via FastAPI's lifespan hook.  This avoids cold-start latency on the first
  real request and lets K8s readiness probes fail fast if files are missing.

• Every endpoint returns a consistent JSON envelope so clients can always
  expect the same top-level shape regardless of success or error.

• Pydantic validates all incoming field types and constraints before the
  request touches the model.  A malformed payload never reaches predict.py.

• The prediction threshold is configurable per-request (default 0.5).
  This lets a calling application tune precision vs recall without
  redeploying the API — e.g. a retention campaign may prefer threshold=0.35
  to catch more at-risk customers.

Run locally
───────────
  uvicorn api.main:app --reload --port 8000

Then open:  http://localhost:8000/docs   (Swagger UI auto-generated by FastAPI)
"""

import logging
import os
from contextlib import asynccontextmanager
from typing import Any, Dict, List, Optional

from fastapi import FastAPI, HTTPException, Query
from fastapi.exceptions import RequestValidationError
from fastapi.responses import JSONResponse
from pydantic import BaseModel, Field

from src.predict import (
    get_artefact_info,
    predict_batch,
    predict_single,
    reload_artefacts,
)
from src.utils import setup_logging

# ---------------------------------------------------------------------------
# Logging — configure once here; all child loggers inherit the format.
# ---------------------------------------------------------------------------
setup_logging(level=os.getenv("LOG_LEVEL", "INFO"))
logger = logging.getLogger(__name__)


# ============================================================================
# Lifespan — startup / shutdown hooks
# ============================================================================
# Using the modern `lifespan` approach instead of the deprecated
# @app.on_event("startup") decorator (removed in FastAPI 0.112+).
#
# Why pre-load at startup?
#   joblib.load() reads ~10–50 MB from disk.  If we lazy-load on the first
#   request, that user gets extra latency.  More importantly, K8s readiness
#   probes hit /health before traffic is sent — we want that probe to fail
#   if the model files are missing, not succeed silently.

@asynccontextmanager
async def lifespan(app: FastAPI):  # noqa: D401
    # ── Startup ──────────────────────────────────────────────────────────
    model_name = os.getenv("MODEL_NAME", "best_model")
    logger.info("Starting up — loading artefacts (model='%s') …", model_name)
    try:
        reload_artefacts(model_name)
        logger.info("Artefacts loaded successfully. API is ready.")
    except FileNotFoundError as exc:
        # Log but do NOT crash — let /health report degraded status instead.
        # A hard crash here would restart the container loop indefinitely.
        logger.error("Artefact load failed at startup: %s", exc)

    yield  # ← application runs here

    # ── Shutdown ─────────────────────────────────────────────────────────
    logger.info("Shutting down.")


# ============================================================================
# FastAPI app
# ============================================================================

app = FastAPI(
    title="Customer Churn Prediction API",
    description=(
        "Predicts whether a telecom customer will churn.\n\n"
        "Use `POST /predict` for single-customer scoring or "
        "`POST /predict-batch` for bulk scoring."
    ),
    version="1.0.0",
    lifespan=lifespan,
)


# ============================================================================
# Custom exception handler — Pydantic validation errors
# ============================================================================
# By default FastAPI returns a 422 with a deeply nested Pydantic error object.
# This handler flattens it into a clean, human-readable list of field errors.

@app.exception_handler(RequestValidationError)
async def validation_error_handler(request, exc: RequestValidationError):
    errors = [
        {
            "field":   " → ".join(str(loc) for loc in err["loc"]),
            "message": err["msg"],
            "type":    err["type"],
        }
        for err in exc.errors()
    ]
    return JSONResponse(
        status_code=422,
        content={
            "error":   "Validation failed",
            "details": errors,
        },
    )


# ============================================================================
# Pydantic schemas
# ============================================================================
# Defined here (not in a separate schemas.py) so the file stays self-contained
# and easy to follow in a code review or interview.

class CustomerFeatures(BaseModel):
    """
    Raw customer features — matches the Telco dataset columns BEFORE any
    preprocessing.  Pydantic enforces types and constraints automatically,
    so invalid payloads are rejected with a 422 before touching the model.
    """
    gender:           str   = Field(..., examples=["Male", "Female"])
    seniorcitizen:    int   = Field(..., examples=[0, 1], ge=0, le=1)
    partner:          str   = Field(..., examples=["Yes", "No"])
    dependents:       str   = Field(..., examples=["No"])
    tenure:           int   = Field(..., examples=[24], ge=0)
    phoneservice:     str   = Field(..., examples=["Yes"])
    multiplelines:    str   = Field(..., examples=["No", "Yes", "No phone service"])
    internetservice:  str   = Field(..., examples=["Fiber optic", "DSL", "No"])
    onlinesecurity:   str   = Field(..., examples=["No"])
    onlinebackup:     str   = Field(..., examples=["Yes"])
    deviceprotection: str   = Field(..., examples=["No"])
    techsupport:      str   = Field(..., examples=["No"])
    streamingtv:      str   = Field(..., examples=["No"])
    streamingmovies:  str   = Field(..., examples=["No"])
    contract:         str   = Field(..., examples=["Month-to-month", "One year", "Two year"])
    paperlessbilling: str   = Field(..., examples=["Yes"])
    paymentmethod:    str   = Field(..., examples=["Electronic check"])
    monthlycharges:   float = Field(..., examples=[70.35], ge=0)
    totalcharges:     float = Field(..., examples=[1687.0], ge=0)

    model_config = {"extra": "ignore"}   # silently drop unknown fields


class PredictionResponse(BaseModel):
    """Single-customer prediction result."""
    churn:       int   = Field(..., description="1 = will churn, 0 = will stay")
    probability: float = Field(..., description="P(churn=1), range 0–1")
    label:       str   = Field(..., description="'Churn' or 'No Churn'")
    threshold:   float = Field(..., description="Probability cutoff used for this prediction")


class BatchPredictRequest(BaseModel):
    """Request body for the batch endpoint."""
    customers: List[CustomerFeatures] = Field(
        ..., description="List of customer records to score", min_length=1
    )
    threshold: float = Field(
        default=0.5,
        description="Probability cutoff applied to every row",
        ge=0.0,
        le=1.0,
    )


class BatchPredictionResponse(BaseModel):
    """Response envelope for the batch endpoint."""
    scored:       int                   = Field(..., description="Number of rows scored")
    predictions:  List[PredictionResponse]


# ============================================================================
# Ops endpoints
# ============================================================================

@app.get(
    "/health",
    tags=["Ops"],
    summary="Liveness probe",
)
def health_check() -> Dict[str, Any]:
    """
    Returns HTTP 200 if the service process is alive.

    Also reports whether the model artefacts are loaded so that K8s
    readiness probes can distinguish "container running" from "model ready".
    """
    info = get_artefact_info()
    ready = info["model_loaded"] and info["preprocessor_loaded"]
    return {
        "status":    "ok" if ready else "degraded",
        "model_ready": ready,
        **info,
    }


@app.get(
    "/model-info",
    tags=["Ops"],
    summary="Readiness / model metadata",
)
def model_info() -> Dict[str, Any]:
    """
    Returns metadata about the currently loaded model artefacts.

    Useful for confirming which model version is serving traffic after
    a deployment — call this from your CI smoke test.
    """
    return {
        "api_version": app.version,
        **get_artefact_info(),
    }


