# Prediction endpoints
# ============================================================================

@app.post(
    "/predict",
    response_model=PredictionResponse,
    tags=["Prediction"],
    summary="Single-customer churn prediction",
)
def predict_endpoint(
    customer: CustomerFeatures,
    threshold: float = Query(
        default=0.5,
        ge=0.0,
        le=1.0,
        description="Probability cutoff: customers with P(churn) ≥ threshold are labelled 'Churn'.",
    ),
) -> PredictionResponse:
    """
    Predict churn probability for a single customer.

    - Pass customer features in the JSON body.
    - Optionally tune the decision threshold via `?threshold=0.35`.
    - Returns predicted class (0/1), probability, and a human-readable label.

    The full preprocessing pipeline (cleaning → feature engineering →
    sklearn transform) is applied internally before the model scores the row.
    """
    try:
        result = predict_single(customer.model_dump(), threshold=threshold)
        return PredictionResponse(**result)

    except FileNotFoundError as exc:
        logger.error("Model artefacts not available: %s", exc)
        raise HTTPException(
            status_code=503,
            detail="Model not ready. Ensure training has completed and artefacts exist.",
        )
    except ValueError as exc:
        # e.g. missing required columns — shouldn't happen here because
        # Pydantic already validated the fields, but kept as a safety net
        raise HTTPException(status_code=422, detail=str(exc))
    except Exception as exc:
        logger.exception("Unexpected error during single prediction.")
        raise HTTPException(status_code=500, detail="Internal prediction error.")


@app.post(
    "/predict-batch",
    response_model=BatchPredictionResponse,
    tags=["Prediction"],
    summary="Batch churn prediction",
)
def predict_batch_endpoint(request: BatchPredictRequest) -> BatchPredictionResponse:
    """
    Score multiple customers in a single HTTP request.

    - Pass a `customers` array in the JSON body (max size limited by your
      load balancer / gateway — no hard limit here).
    - One optional `threshold` applies to every row in the batch.
    - Returns a `predictions` array in the same order as the input.

    Internally calls `predict_batch()` which runs the full preprocessing
    pipeline once across all rows — far more efficient than N individual
    `/predict` calls for large batches.
    """
    try:
        records = [c.model_dump() for c in request.customers]

        # predict_batch accepts a list of dicts directly
        result_df = predict_batch(records, threshold=request.threshold)

        predictions = [
            PredictionResponse(
                churn=int(row["churn_prediction"]),
                probability=float(row["churn_probability"]),
                label=str(row["churn_label"]),
                threshold=request.threshold,
            )
            for _, row in result_df.iterrows()
        ]

        return BatchPredictionResponse(
            scored=len(predictions),
            predictions=predictions,
        )

    except FileNotFoundError as exc:
        logger.error("Model artefacts not available: %s", exc)
        raise HTTPException(
            status_code=503,
            detail="Model not ready. Ensure training has completed and artefacts exist.",
        )
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc))
    except Exception as exc:
        logger.exception("Unexpected error during batch prediction.")
        raise HTTPException(status_code=500, detail="Internal prediction error.")
